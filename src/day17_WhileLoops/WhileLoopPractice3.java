package day17_WhileLoops;
public class WhileLoopPractice3 {
    public static void main(String[] args) {
        int i = 1;
        while (i < 101) {
        }
        System.out.println("*");
        i++;
        System.out.println();

        int number = 0;
        System.out.println("*******");
        while (number < 3) { //0,1,2
            System.out.println("*    *");
            number++;
        }
        System.out.println("*******");
    }
}
